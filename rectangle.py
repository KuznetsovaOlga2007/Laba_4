def area(a, b):
    '''
    Возвращает площадь прямоугольника.

        Параметры:
            a (int): сторона прямоугольника
            b (int): сторона прямоугольника

        Возвращаемое значение:
            a * b: площадь прямоугольника со сторонами a и b.
    '''
    return a * b 

