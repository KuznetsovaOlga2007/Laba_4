import math

def area(a,b):
    '''
    Функция принимает на вход 2 стороны прямоугольника и возвращает его площадь
    Пример: area(5,2) = 5 * 2 = 10
    '''
    return a*b

def perimeter(a,b):
    '''
    Функция принимает на вход 2 стороны прямоугольника и возвращает его периметр
    Пример: perimeter(4,5) = 2 * 4 + 2 * 5 = 18
    '''
    return 2*a + 2*b
    