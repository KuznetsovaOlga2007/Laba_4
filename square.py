
def area(a):
    '''
    Возвращает площадь квадрата.

        Параметры:
            a (int): длина квадрата
        
        Возвращаемое значение:
            a * a (int): искомая площадь квадрата.
    '''
    return a * a


def perimeter(a):
    '''
    Возвращает периметр квадрата.

        Параметры:
            a (int): длина квадрата
        
        Возвращаемое значение:
            4 * a (int): искомый периметр квадрата.
    '''
    return 4 * a
