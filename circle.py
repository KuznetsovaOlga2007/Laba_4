import math


def area(r):
    ''' Функция вычисляет площадь круга с помощью специальной формулы, которая использует радиус r'''
    return math.pi * r * r


def perimeter(r):
    ''' Функция вычисляет периметр круга с помощью специальной формулы, которая использует радиус r'''
    return 2 * math.pi * r
