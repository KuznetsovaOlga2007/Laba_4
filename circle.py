import math


def area(r):
    """Returns the area of a circle of the given radius.
            Example: area(5) = 78.53981633974483
    """
    return math.pi * r * r


def perimeter(r):
    """Returns the perimeter of a circle of the given radius.
            Example: perimeter(3346457) = 6692914.999999999
    """
    return 2 * math.pi * r

