import math

def area(r):
    return math.pi * r * r
''' Находит площадь окружности
    Параметры:
        r (double): радиус окружности
    Возвращаемые значения:
        area (double): площадь окружности
'''

def perimeter(r):
    return 2 * math.pi * r
