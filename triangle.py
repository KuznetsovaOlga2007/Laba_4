def area(a, h): 
    '''Принимает два числа: a - длину стороны треугольника и h - длину высоты, проведенной к стороне a, возвращает площадь этого треугольника'''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает три числа a, b и c - длины сторон треугольника, возвращает периметр этого треугольника'''
    return a + b + c 