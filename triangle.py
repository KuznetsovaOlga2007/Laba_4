def area(a, h): 
    '''
    Функция принимает на вход сторону и высоту треугольника, возвращает его площадь
    Пример: area(5,2) = 5 * 2 / 2 = 5
    '''
    return a * h / 2 

def perimeter(a, b, c): 
    '''
    Функция принимает на вход стороны треугольника и возвращает его периметер
    Пример: perimeter(3,4,5) = 3 + 4 + 5 = 12
    '''
    return a + b + c 