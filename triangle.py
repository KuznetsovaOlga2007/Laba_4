def area(a, h):
    '''Функция принимает значения основания и высоты треугольника и вычисляет его площадь.
    Принимает и возвращает значения int'''
    return a * h / 2

def perimeter(a, b, c):
    '''Функция принимает значения сторон треугольника и вычисляет его периметр.
    Принимает и возвращает значения int'''
    return a + b + c
