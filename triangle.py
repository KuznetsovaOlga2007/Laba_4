def area(a, h):
	'''принимает переменные a - сторона и h - высота и возвращает значение площади.
	По формуле S = a * h / 2 находится площадь
	'''
	return a * h / 2
def perimeter(a, b, c):
	'''ринимает переменные **a**, **b**, **c** - *являющиеся сторонами треугольника* и возвращает значение периметра треугольника.
	По формуле P = a + b + c находится периметр
	'''
	return a + b + c

