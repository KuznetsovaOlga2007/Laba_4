def area(a, h): 
    '''Принемает числа a и h - основание и высота, возвращает число площадь треугольника с основанием и высотой a и h'''
    return a * h / 2 

def perimeter(a, b, c):
    '''Принемает числа a, b и c  - стороны треугольника, возвращает число периметр треугольника со сторонами a, b и c'''
    return a + b + c